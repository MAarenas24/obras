    private void establecerTipoDocumentacion(String nif) {
        String tipoDocumento = "NIF";

        String nifLimpio = nif.replace("_", "").trim();

        if (nifLimpio.length() >= 1) {
            char primerCaracter = nifLimpio.toUpperCase().charAt(0);

            if (primerCaracter == 'X' || primerCaracter == 'Y' || primerCaracter == 'Z') {
                tipoDocumento = "NIE";
            } else if (Character.isLetter(primerCaracter)) {
                tipoDocumento = "CIF";
            } else if (nifLimpio.length() < 9) {

            }

        }

        jComboBoxMnto1.setSelectedItem(tipoDocumento);

        MaskFormatter formatter;
        formatter = null;
        try {
            switch (tipoDocumento) {
                case "NIF":
                    formatter = new MaskFormatter("########U");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "CIF":
                    formatter = new MaskFormatter("U########");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "NIE":
                    formatter = new MaskFormatter("U#######U");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "Libre":
                    formatter = new MaskFormatter("*********");
                    formatter.setPlaceholderCharacter('_');
                    break;
            }

            jFormattedTextFieldDocumentacion.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(formatter));
            jFormattedTextFieldDocumentacion.setText(nif);
        } catch (ParseException ex) {
            System.err.println("Error al aplicar máscara: " + ex.getMessage());
        }
    }

    private void setCamposFromTable() {
        int filaSeleccionada = jTableTrabajadores.getSelectedRow();

        if (filaSeleccionada >= 0) {
            DBTrabajadores trabajador = listaTrabajadores.get(filaSeleccionada);

            establecerTipoDocumentacion(trabajador.getNif());

            jTextFieldNombreTrabajador.setText(trabajador.getNombre());
            jTextFieldPoblacion.setText(trabajador.getPoblacion());
            jSpinnerPvpHora.setValue(trabajador.getPvpHora());

            int tipo = trabajador.getTipo();

            if (tipo == 0) {
                jRadioButtonAdministracion.setSelected(true);
                jRadioButtonServicios.setSelected(false);
            } else if (tipo == 1) {
                jRadioButtonServicios.setSelected(true);
                jRadioButtonAdministracion.setSelected(false);

            } else {
                buttonGroup2.clearSelection();
            }
        }
    }

    private void jButtonModificarTrabajadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarTrabajadoresActionPerformed
        // TODO add your handling code here:
        String nombre = jTextFieldNombreTrabajador.getText();
        String dni = jFormattedTextFieldDocumentacion.getText();
        int tipo;

        if (jRadioButtonAdministracion.isSelected()) {
            tipo = 0;
        } else if (jRadioButtonServicios.isSelected()) {
            tipo = 1;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Tipo de trabajador.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int pvpHora = (int) jSpinnerPvpHora.getValue();
        String poblacion = jTextFieldPoblacion.getText();

        if (nombre.isEmpty() || dni.isEmpty() || dni.contains("_") || poblacion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos obligatorios deben estar llenos y el NIF completo.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DBTrabajadores trabajadorModificar = new DBTrabajadores(dni, nombre, poblacion, null, tipo, pvpHora);

        try {
            DMTrabajadores query = new DMTrabajadores();
            System.out.println("haciendo la query");
            query.modificarTrabajador(trabajadorModificar);
            System.out.println("Qeury hecha");
            JOptionPane.showMessageDialog(this, "Trabajador modificado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

            limpiarCampos();
            cargarTrabla();

            jButtonCancelarActionPerformed(evt);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error de base de datos al modificar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonModificarTrabajadoresActionPerformed

