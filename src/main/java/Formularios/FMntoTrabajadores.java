    private void jComboBoxMnto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxMnto1ActionPerformed
        // TODO add your handling code here:
        String seleccion = (String) jComboBoxMnto1.getSelectedItem();
        MaskFormatter formatter;
        formatter = null;
        try {
            switch (seleccion) {
                case "NIF":
                    formatter = new MaskFormatter("########U");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "CIF":
                    formatter = new MaskFormatter("U########");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "NIE":
                    formatter = new MaskFormatter("U#######U");
                    formatter.setPlaceholderCharacter('_');
                    break;
                case "Libre":
                    formatter = new MaskFormatter("*********");
                    formatter.setPlaceholderCharacter('_');
                    break;
            }

            jFormattedTextFieldDocumentacion.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(formatter));
            jFormattedTextFieldDocumentacion.setValue(null);
        } catch (ParseException ex) {
            // Esto solo se ejecuta si defines mal una máscara (ej: "###" en vez de "###")

        }
    }//GEN-LAST:event_jComboBoxMnto1ActionPerformed

