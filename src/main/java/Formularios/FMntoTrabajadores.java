    public void cargarTrabla() {
        try {
            DMTrabajadores cargarTrabajadores = new DMTrabajadores();
            listaTrabajadores = cargarTrabajadores.jcbCargaComboTrabajadores();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los trabajadores", "ERROR", JOptionPane.ERROR);
        }

        modelTrabla.setRowCount(0);
        modelTrabla.setNumRows(listaTrabajadores.size());

        for (int fila = 0; fila < modelTrabla.getRowCount(); fila++) {
            DBTrabajadores trabajador = listaTrabajadores.get(fila);

            String tipo = "";
            if (trabajador.getTipo() == 0) {
                tipo = "Administrativo";
            } else {
                tipo = "Servicios";
            }

            modelTrabla.setValueAt(listaTrabajadores.get(fila).getNif(), fila, 0);
            modelTrabla.setValueAt(listaTrabajadores.get(fila).getNombre(), fila, 1);
            modelTrabla.setValueAt(listaTrabajadores.get(fila).getPoblacion(), fila, 2);
            modelTrabla.setValueAt(tipo, fila, 3);
            modelTrabla.setValueAt(listaTrabajadores.get(fila).getPvpHora(), fila, 4);
        }
    }

