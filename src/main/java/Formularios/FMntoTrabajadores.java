    private void jButtonEliminarTrabajadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarTrabajadoresActionPerformed
        // TODO add your handling code here:
        String nifAEliminar = jFormattedTextFieldDocumentacion.getText().replace("_", "").trim();

        if (nifAEliminar.isEmpty() || nifAEliminar.length() < 8) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un trabajador (NIF/CIF/NIE incompleto).", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(
                this,
                "Estas seguro de eliminar permanentemente al trabajador",
                "Confirmar Eliminacion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (respuesta == JOptionPane.YES_OPTION) {
            try {
                DMTrabajadores query = new DMTrabajadores();
                boolean eliminado = query.eliminarTrabajadores(nifAEliminar);

                if (eliminado) {
                    JOptionPane.showMessageDialog(this,
                            "Trabajador eliminado con éxito.",
                            "Eliminación Correcta",
                            JOptionPane.INFORMATION_MESSAGE);

                    limpiarCampos();
                    cargarTrabla();
                    jButtonCancelarActionPerformed(evt);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "No se pudo eliminar el trabajador: tiene servicios asociados o no existe.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,
                        "Error de Base de Datos al eliminar: " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonEliminarTrabajadoresActionPerformed

