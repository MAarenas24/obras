    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        int index = cbTrabajadores.getSelectedIndex();
        if (index == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un trabajador.");
            return;
        }

        if (lista.get(index).getNif().equals(String.valueOf(jPasswordFieldContrasenia.getPassword()))) {

            FMenuPrincipal.usuario = new DBTrabajadores(lista.get(index).getNif(), lista.get(index).getNombre(), lista.get(index).getPoblacion(), lista.get(index).getDescripcion(), lista.get(index).getTipo(), lista.get(index).getPvpHora());

            loginCorrecto = true;
            JOptionPane.showMessageDialog(this, "Contrase침a correcta, Has entrado correctamente", "ACCESO ACEPTADO", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Contrase침a incorrecta, no has podido conectarse", "ACCESO DENEGADO", JOptionPane.ERROR_MESSAGE);
        }

        this.dispose();

    }//GEN-LAST:event_jButtonAceptarActionPerformed

