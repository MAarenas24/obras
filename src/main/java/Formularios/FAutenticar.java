    private void cargarTrabajadores() {
        try {
            DMTrabajadores query = new DMTrabajadores();
            lista = query.jcbCargaComboTrabajadores();

            cbTrabajadores.removeAllItems(); // Limpia el combo antes de cargar
            for (DBTrabajadores t : lista) {
                cbTrabajadores.addItem(t.getDescripcion()); // Gracias al toString() se mostrar치 bien
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al cargar trabajadores: " + e.getMessage(),
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
